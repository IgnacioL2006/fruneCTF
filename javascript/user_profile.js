function toggleDescForm() {
    const f = document.getElementById('desc-form');
    f.style.display = (f.style.display === 'none' || f.style.display === '') ? 'block' : 'none';
}

function toggleImgForm() {
    const f = document.getElementById('img-form');
    f.style.display = (f.style.display === 'none' || f.style.display === '') ? 'block' : 'none';
}
